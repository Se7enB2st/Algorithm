# Area formulas in Ruby

# Create a method that finds the area of circle given the radius
def area_of_circle(radius)
  Math::PI*(radius*radius)
end

# Create a method that finds the area of square given the length
def area_of_square(length)
	length*length
end 

# Create a method that finds the area of rectangle given the width and length
def area_of_rectangle(width, length)
	width*length
end 


